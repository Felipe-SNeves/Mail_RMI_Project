    private void botaoEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoEnviarActionPerformed
        
        Servicos_mensagem sdao = null;
        JFrame telinha = new JFrame("Mensagem");
        
        try{
            sdao = (Servicos_mensagem) Naming.lookup("//192.168.0.13/servico_mensagem");
            sdao.enviar(this.getEmail() + this.getDominio(), inputDestinatario.getText(), inputAssunto.getText(), inputCorpo.getText());
            
            Enviar n_tela = new Enviar();
            n_tela.recebendoDados(this.getEmail(), this.getSenha(), this.getDominio());
            JOptionPane.showMessageDialog(telinha, "Mensagem Enviada!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
           
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(telinha, e.getMessage(), "Erro", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_botaoEnviarActionPerformed

