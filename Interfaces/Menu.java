    private void botaoExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoExcluirActionPerformed
        
        int retorno;
        JFrame frame = new JFrame("Mensagem");
        
        retorno = JOptionPane.showConfirmDialog(frame, "Tem certeza que deseja excluir a conta?", "Exclus√£o", JOptionPane.YES_NO_OPTION);
        
        if(retorno == JOptionPane.YES_OPTION){
            try{
                
                Servicos_usuario uremoto = (Servicos_usuario) Naming.lookup("//192.168.0.13/servico_usuario");
            
                uremoto.excluir(this.getEmail(), this.getDominio());
                Autenticacao tela = new Autenticacao();
                this.dispose();
                JOptionPane.showMessageDialog(frame, "Conta excluida com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(frame, e.getMessage(), "Erro", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_botaoExcluirActionPerformed

